def get_chunk_index(traj_index):
    """
    0 -> trajectory 0 (30 points)
    1 -> trajectories 1..9 (9 of them, 25 points)
    2 -> trajectories 10..99 (90 of them, 20 points)
    3 -> trajectories 100..999 (900 of them, 15 points)
    4 -> trajectories 1000..9999 (9000 of them, 10 points)
    """
    if traj_index == 0:
        return 0
    elif 1 <= traj_index <= 9:
        return 1
    elif 10 <= traj_index <= 99:
        return 2
    elif 100 <= traj_index <= 999:
        return 3
    else:
        return 4

def compute_bspline(points):
    from scipy.interpolate import splprep, splev
    x = points[:, 0]
    y = points[:, 1]
    # s=0 for no smoothing
    tck, u = splprep([x, y], s=0)
    u_dense = np.linspace(0, 1, 5000)
    x_spline, y_spline = splev(u_dense, tck)
    return x_spline, y_spline

