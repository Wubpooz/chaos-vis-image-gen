def hash_to_args(hash, password=None):
    if not password:
        password = PASSWORD

    combined = base64.urlsafe_b64decode(hash.encode('utf-8'))
    salt, nonce, tag, ciphertext = combined[:16], combined[16:28], combined[28:44], combined[44:]
    kdf = PBKDF2HMAC(
        algorithm=hashes.SHA256(),
        length=32,
        salt=salt,
        iterations=100000,
        backend=default_backend()
    )
    key = kdf.derive(password.encode())
    cipher = Cipher(algorithms.AES(key), modes.GCM(nonce, tag), backend=default_backend())
    decryptor = cipher.decryptor()
    serialized = decryptor.update(ciphertext) + decryptor.finalize()
    args = pickle.loads(serialized)

    return {
        **{f"a_{i + 1}": args[i] for i in range(6)},
        **{f"b_{i + 1}": args[i + 6] for i in range(6)},
        "x_0": args[-6],
        "y_0": args[-5],
        "x_min": args[-4],
        "y_min": args[-3],
        "x_max": args[-2],
        "y_max": args[-1],
    }

@njit
def compute_trajectory(ax, ay, x0, y0, xmin, xmax, ymin, ymax, max_iter, d0, initial_step, n_points, lyapunov_threshold):
    """
    Computes the trajectory of the attractor and the Lyapunov index.

    Parameters:
    - ax, ay: Coefficients for the equations of motion.
    - x0, y0: Initial conditions for the attractor.
    - xmin, xmax, ymin, ymax: Bounds for the system.
    - max_iter: Maximum number of iterations.
    - d0: Initial distance between trajectories for Lyapunov calculation.
    - initial_step: Perturbed initial step for Lyapunov calculation.
    - n_points: Number of points to retain from the end of the trajectory.
    - lyapunov_threshold: Minimum value of Lyapunov index for success.

    Returns:
    - success: Whether the attractor meets all conditions.
    - lyapunov: The computed Lyapunov index.
    - x_vals_final, y_vals_final: Arrays of the last `n_points` trajectory points.
    """
    # Allocate arrays for storing trajectory values
    x_vals = np.zeros(max_iter, dtype=np.float64)
    y_vals = np.zeros(max_iter, dtype=np.float64)
    x_vals[0], y_vals[0] = x0, y0

    # Initialize Lyapunov index and perturbed trajectory
    lyapunov = 0.0
    xe, ye = initial_step

    # Iterate to compute trajectory and Lyapunov index
    for i in range(1, max_iter):
        # Update primary system equations
        x_vals[i] = (ax[0] + ax[1] * x_vals[i-1] + ax[2] * x_vals[i-1]**2 +
                     ax[3] * x_vals[i-1] * y_vals[i-1] + ax[4] * y_vals[i-1] +
                     ax[5] * y_vals[i-1]**2)
        y_vals[i] = (ay[0] + ay[1] * x_vals[i-1] + ay[2] * x_vals[i-1]**2 +
                     ay[3] * x_vals[i-1] * y_vals[i-1] + ay[4] * y_vals[i-1] +
                     ay[5] * y_vals[i-1]**2)

        # Check bounds
        if x_vals[i] < xmin or x_vals[i] > xmax or y_vals[i] < ymin or y_vals[i] > ymax:
            return False, lyapunov, None, None

        # Check for convergence
        dx = x_vals[i] - x_vals[i-1]
        dy = y_vals[i] - y_vals[i-1]
        if abs(dx) < 1e-10 and abs(dy) < 1e-10:
            return False, lyapunov, None, None

        # Start computing Lyapunov after 2000 iterations
        if i > 2000:
            # Update perturbed system equations
            xenew = (ax[0] + ax[1] * xe + ax[2] * xe**2 + ax[3] * xe * ye +
                     ax[4] * ye + ax[5] * ye**2)
            yenew = (ay[0] + ay[1] * xe + ay[2] * xe**2 + ay[3] * xe * ye +
                     ay[4] * ye + ay[5] * ye**2)

            # Calculate Lyapunov divergence
            dx_lyap = x_vals[i] - xenew
            dy_lyap = y_vals[i] - yenew
            dd = np.sqrt(dx_lyap**2 + dy_lyap**2)
            if dd > 0:
                lyapunov += np.log(abs(dd / d0))
                # Re-normalize perturbed trajectory
                xe = x_vals[i] + d0 * dx_lyap / dd
                ye = y_vals[i] + d0 * dy_lyap / dd

    # Check Lyapunov threshold
    if lyapunov < lyapunov_threshold:
        return False, lyapunov, None, None

    # Return the last `n_points` trajectory values
    return True, lyapunov, x_vals[-n_points:], y_vals[-n_points:]

def test_single_attractor(args, output_dir, max_iter=10**6, debug=False, lyapunov_threshold=5.0, plot_size_threshold=70 * 1024):
    """
    Attempts to generate a random attractor. If it meets certain conditions,
    saves and returns the directory. Otherwise, returns None.

    Parameters:
    - args: Dictionary of parameters for the attractor.
    - output_dir: Directory to save the attractor plot.
    - max_iter: Maximum number of iterations.
    - debug: Whether to print debug messages.
    - lyapunov_threshold: Minimum Lyapunov index required.
    - plot_size_threshold: Minimum plot file size in bytes required.

    Returns:
    - success: True if the attractor meets all conditions, False otherwise.
    """

    def debug_print(s):
        # Print debug messages if debug mode is enabled
        if debug:
            print(s)

    # Create output directory if it doesn't exist
    os.makedirs(output_dir, exist_ok=True)

    # Extract parameters from args
    ax = np.array([args[f'a_{i}'] for i in range(1, 7)], dtype=np.float64)
    ay = np.array([args[f'b_{i}'] for i in range(1, 7)], dtype=np.float64)
    x0, y0 = args['x_0'], args['y_0']
    xmin, ymin = args['x_min'], args['y_min']
    xmax, ymax = args['x_max'], args['y_max']

    # Add border to bounds
    x_border = 0.05 * (xmax - xmin)
    y_border = 0.2 * (ymax - ymin)
    xmin -= x_border
    xmax += x_border
    ymin -= y_border
    ymax += y_border

    # Initialize perturbed trajectory for Lyapunov calculation
    while True:
        xe = x0 + (np.random.rand() - 0.5) / 1000.0
        ye = y0 + (np.random.rand() - 0.5) / 1000.0
        dx = x0 - xe
        dy = y0 - ye
        d0 = np.sqrt(dx**2 + dy**2)
        if d0 > 0:
            break

    # Compute trajectory and Lyapunov index
    n_points = 100000  # Number of points to retain for plotting
    success, lyapunov, x_vals_final, y_vals_final = compute_trajectory(
        ax, ay, x0, y0, xmin, xmax, ymin, ymax, max_iter, d0, np.array([xe, ye]), n_points, lyapunov_threshold
    )

    if not success:
        debug_print("BAD CASE: Condition not met during trajectory computation.")
        return False

    debug_print(f"Lyapunov Index (Chaotic Index) is {lyapunov:.2f} or {(lyapunov / lyapunov_threshold * 100):.2f}% of threshold")
    debug_print(f"Lyapunov Index (Chaotic Index) Threshold is {lyapunov_threshold:.2f}")

    # Generate and save the attractor plot
    fig, ax_plot = plt.subplots(figsize=(5, 5), dpi=100)
    ax_plot.set_xlim(xmin, xmax)
    ax_plot.set_ylim(ymin, ymax)
    ax_plot.scatter(x_vals_final, y_vals_final, s=0.1, color='black')
    ax_plot.axis('off')
    fname_image = os.path.join(output_dir, "plot.png")
    plt.savefig(fname_image, bbox_inches='tight', pad_inches=0)
    plt.close()

    # Check plot size
    plot_size = os.path.getsize(fname_image)
    debug_print(f"Plot Size is {plot_size} bytes or {(plot_size / plot_size_threshold * 100):.2f}% of threshold")
    debug_print(f"Plot Size Threshold is {plot_size_threshold} bytes")

    if plot_size < plot_size_threshold:
        debug_print("BAD CASE: Visually Not Interesting")
        return False

    return True

