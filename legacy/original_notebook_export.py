@njit
def compute_grid_and_last_position(x0, y0, ax, ay, xmin, xmax, ymin, ymax, x_scale, y_scale, grid, num_iterations):
    """
    Numba-optimized function to compute the density grid and return the last position.

    Args:
        x0, y0: Initial coordinates.
        ax, ay: Coefficients for the attractor equations.
        xmin, xmax, ymin, ymax: Grid boundaries.
        x_scale, y_scale: Precomputed scaling factors.
        grid: The 2D density grid.
        num_iterations: Number of iterations to perform.

    Returns:
        Updated grid and final x, y positions after iterations.
    """
    x, y = x0, y0
    for _ in range(num_iterations):
        # Map to grid indices
        ix = int((x - xmin) * x_scale)
        iy = int((y - ymin) * y_scale)

        # Increment grid cell if within bounds
        if 0 <= ix < grid.shape[1] and 0 <= iy < grid.shape[0]:
            grid[grid.shape[0] - iy - 1, ix] += 1.0

        # Update coordinates using attractor equations
        x_next = (ax[0] + ax[1] * x + ax[2] * x * x +
                  ax[3] * x * y + ax[4] * y + ax[5] * y * y)
        y_next = (ay[0] + ay[1] * x + ay[2] * x * x +
                  ay[3] * x * y + ay[4] * y + ay[5] * y * y)
        x, y = x_next, y_next

    return grid, x, y

def simulate_attractor(args, output_dir, overwrite=False, max_iterations=100_000_000, grid_size=(1920, 1080)):
    """
    Process the attractor density map from a hash, evaluate the attractor path,
    and save the resulting 2D density grid as a pickle file.

    Args:
        args (str): The dictionary containing attractor parameters.
        output_dir (str): Directory to save the output pickle file.
        max_iterations (int): Number of iterations to evaluate the attractor path.
        grid_size (int): Size of the 2D grid for the histogram.
    """
    os.makedirs(output_dir, exist_ok=True)
    log_file = os.path.join(output_dir, f"density_processing_log.txt")
    pickle_file = os.path.join(output_dir, f"density_grid.pkl")
    if not overwrite and os.path.exists(pickle_file):
        print("Pickle file already exists at:", pickle_file)
        return pickle_file

    ax = [args[f'a_{i}'] for i in range(1, 7)]
    ay = [args[f'b_{i}'] for i in range(1, 7)]
    x0, y0, xmin, ymin, xmax, ymax = args['x_0'], args['y_0'], args['x_min'], args['y_min'], args['x_max'], args['y_max']

    x_border = 0.05 * (xmax - xmin)
    y_border = 0.2 * (ymax - ymin)
    xmin -= x_border
    xmax += x_border
    ymin -= y_border * 0.5
    ymax += y_border * 1.5

    x_scale = (grid_size[1] - 1) / (xmax - xmin)
    y_scale = (grid_size[0] - 1) / (ymax - ymin)

    grid = np.zeros(grid_size, dtype=np.float32)
    chunk_size = 10_000_000

    with open(log_file, "w", encoding="utf-8") as log:
        log.write("Processing density grid:\n")
        origin_time = time.time()

        total_chunks = max_iterations // chunk_size
        remaining_iterations = max_iterations % chunk_size

        for chunk in tqdm(range(total_chunks)):
            grid, x0, y0 = compute_grid_and_last_position(
                x0, y0, ax, ay, xmin, xmax, ymin, ymax, x_scale, y_scale, grid, chunk_size
            )
            current_time = time.time()
            elapsed_time = current_time - origin_time
            avg_time = elapsed_time / ((chunk + 1) * chunk_size)
            remaining_time = avg_time * (max_iterations - (chunk + 1) * chunk_size)
            message = (
                f"Iteration {(chunk + 1) * chunk_size}/{max_iterations}\n"
                f"Elapsed Time: {elapsed_time:.2f} seconds\n"
                f"Average Time per Iteration: {avg_time:.6f} seconds\n"
                f"Estimated Time Remaining: {remaining_time:.2f} seconds\n\n"
            )
            log.write(message)

        if remaining_iterations > 0:
            grid, x0, y0 = compute_grid_and_last_position(
                x0, y0, ax, ay, xmin, xmax, ymin, ymax, x_scale, y_scale, grid, remaining_iterations
            )
            current_time = time.time()
            elapsed_time = current_time - origin_time
            avg_time = elapsed_time / max_iterations
            message = (
                f"Iteration {max_iterations}/{max_iterations}\n"
                f"Elapsed Time: {elapsed_time:.2f} seconds\n"
                f"Average Time per Iteration: {avg_time:.6f} seconds\n\n"
            )
            log.write(message)

        with open(pickle_file, 'wb') as f:
            pickle.dump(grid, f)

        final_message = f"Processing complete. Density grid saved to: {pickle_file}\n"
        print(final_message)
        log.write(final_message)

    return pickle_file

