# We'll now sample discrete trajectories with updated sizes:
#   chunk 0: 30 points  (was 25)
#   chunk 1: 25 points  (was 20)
#   chunk 2: 20 points  (was 15)
#   chunk 3: 15 points  (was 10)
#   chunk 4: 10 points  (was 5)
# We also add a final point at the center (so effectively the last point is the center).
############################################################

def sample_discrete_trajectories(x_vals, y_vals, center_x, center_y, minX, maxX, minY, maxY):
    """
    We'll create 1 + 9 + 90 + 900 + 9000 = 10,000 trajectories as specified,
    but each is 5 points longer than before.
      chunk 0: 1 trajectory, 30 points
      chunk 1: 9 trajectories, 25 points
      chunk 2: 90 trajectories, 20 points
      chunk 3: 900 trajectories, 15 points
      chunk 4: 9000 trajectories, 10 points
    We'll take the first (n-1) samples from the attractor, and add
    one extra final point at the center.
    """
    num_iter = len(x_vals)
    trajectories = []

    # We'll define how many trajectories and how many points each chunk has:
    # chunk 0 -> 1 trajectory of 30 points
    # chunk 1 -> 9 trajectories of 25 points
    # chunk 2 -> 90 trajectories of 20 points
    # chunk 3 -> 900 trajectories of 15 points
    # chunk 4 -> 9000 trajectories of 10 points
    # in total 10,000 trajectories.

    def generate_random_points(x, y, k):
        # Randomly sample an angle between 0 and 2π
        theta = random.uniform(0, 2 * math.pi)
        # Calculate the new point coordinates
        new_x = x + k * math.cos(theta)
        new_y = y + k * math.sin(theta)
        return new_x, new_y

    # The sample_trajectory function picks (n-1) from the attractor, appends center
    def sample_trajectory(n_points, offset):
        # n_points includes the final center point, so we only gather (n_points - 1) from the attractor
        n_gather = n_points - 1
        pts = np.zeros((n_points, 2), dtype=np.float64)
        step = (num_iter - 1 - offset) // (n_gather - 1) if n_gather > 1 else 1
        idx = offset
        for i in range(n_gather):
            if idx >= num_iter:
                idx = num_iter - 1
            pts[i, 0] = x_vals[idx]
            pts[i, 1] = y_vals[idx]
            idx += step
        # The final point is the center
        x_rand = random.uniform(minX, maxX)
        pts[0, 0], pts[0, 1] = x_rand, minY - 0.1*(maxY-minY)
        pts[1, 0], pts[1, 1] = x_rand, minY + 0.1*(maxY-minY)
        pts[-4, 0], pts[-4, 1] = x_rand, maxY + 0.1*(maxY-minY)
        pts[-3, 0], pts[-3, 1] = random.uniform(minX, maxX), maxY + 0.3*(maxY-minY)
        pts[-2, 0], pts[-2, 1] = random.uniform(minX, maxX), maxY + 1*(maxY-minY)
        pts[-1, 0], pts[-1, 1] = random.uniform(minX, maxX), maxY + 2*(maxY-minY)
        return pts

    offset_counter = 0

    add_ons = 20
    step_size = 3

    # chunk 0
    t_30 = sample_trajectory(step_size*5 + add_ons, offset_counter)
    offset_counter += 100
    trajectories.append(t_30)

    # chunk 1
    for _ in range(9):
        t_25 = sample_trajectory(step_size*4 + add_ons, offset_counter)
        offset_counter += 100
        trajectories.append(t_25)

    # chunk 2
    for _ in range(90):
        t_20 = sample_trajectory(step_size*3 + add_ons, offset_counter)
        offset_counter += 100
        trajectories.append(t_20)

    # chunk 3
    for _ in range(900):
        t_15 = sample_trajectory(step_size*2 + add_ons, offset_counter)
        offset_counter += 100
        trajectories.append(t_15)

    # chunk 4
    for _ in range(1000):
        t_10 = sample_trajectory(step_size + add_ons, offset_counter)
        offset_counter += 100
        trajectories.append(t_10)

    return trajectories

