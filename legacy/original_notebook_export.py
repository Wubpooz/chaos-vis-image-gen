@njit
def iterate_attractor(ax, ay, x0, y0, num_iter=10_000_000):
    """
    Given the attractor coefficients ax, ay, initial conditions x0, y0,
    iterate the system for num_iter steps and return arrays of shape (num_iter,).
    """
    x_vals = np.zeros(num_iter, dtype=np.float64)
    y_vals = np.zeros(num_iter, dtype=np.float64)
    x_vals[0], y_vals[0] = x0, y0
    for i in range(1, num_iter):
        x_vals[i] = (ax[0] + ax[1]*x_vals[i-1] + ax[2]*x_vals[i-1]**2 +
                     ax[3]*x_vals[i-1]*y_vals[i-1] + ax[4]*y_vals[i-1] +
                     ax[5]*y_vals[i-1]**2)
        y_vals[i] = (ay[0] + ay[1]*x_vals[i-1] + ay[2]*x_vals[i-1]**2 +
                     ay[3]*x_vals[i-1]*y_vals[i-1] + ay[4]*y_vals[i-1] +
                     ay[5]*y_vals[i-1]**2)
    return x_vals, y_vals

