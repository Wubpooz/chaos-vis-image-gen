def save_attractor(password, a, b, xmin, xmax, ymin, ymax, x_vals, y_vals, base_output_dir):
    """
    Save the attractor with density-based coloring and smoothing in a hash-based directory.
    If the image is smaller than 80KB, deletes all newly created directories up to base_output_dir.
    """
    import matplotlib
    matplotlib.use("Agg")  # So we can save images in headless mode

    # Save the parameters and generate encrypted string
    params = list(a) + list(b) + [x_vals[0], y_vals[0], xmin, ymin, xmax, ymax]
    encrypted_string = encrypt_params(params, password)

    # Create hash-based directory structure
    hash_dir = split_hash_into_dirs(encrypted_string)
    output_dir = os.path.join(base_output_dir, hash_dir)

    # Keep track of the polish directory for cleanup
    polish_dir = os.path.join(base_output_dir)

    # Create the directory if it doesn't exist
    os.makedirs(output_dir, exist_ok=True)

    # Save parameters to text file
    fname_params = os.path.join(output_dir, "params.txt")
    with open(fname_params, 'w') as f:
        f.write(encrypted_string + "\n")
        f.write("\n# Attractor parameters and bounds\n")
        f.write(f"xmin: {xmin}\n")
        f.write(f"ymin: {ymin}\n")
        f.write(f"xmax: {xmax}\n")
        f.write(f"ymax: {ymax}\n")

        f.write("\n# Coefficients for the attractor equations\n")
        f.write("# x_next = ax[0] + ax[1]*x + ax[2]*x^2 + ax[3]*x*y + ax[4]*y + ax[5]*y^2\n")
        f.write("# y_next = ay[0] + ay[1]*x + ay[2]*x^2 + ay[3]*x*y + ay[4]*y + ay[5]*y^2\n")
        for i, (ai, bi) in enumerate(zip(a, b)):
            f.write(f"ax[{i}]: {ai}\n")
            f.write(f"ay[{i}]: {bi}\n")

        f.write("\n# Initial conditions\n")
        f.write(f"x0: {x_vals[0]}\n")
        f.write(f"y0: {y_vals[0]}\n")

    # Save the attractor image
    width, height = 500, 500
    fig, ax = plt.subplots(figsize=(5, 5), dpi=100)
    ax.set_xlim(xmin, xmax)
    ax.set_ylim(ymin, ymax)
    ax.scatter(x_vals[100:], y_vals[100:], s=0.1, color='black')
    ax.axis('off')
    fname_image = os.path.join(output_dir, "plot.png")
    plt.savefig(fname_image, bbox_inches='tight', pad_inches=0)
    plt.close()

    # Check image size and delete directories if too small
    try:
        size_bytes = os.path.getsize(fname_image)
    except:
        # If there's some error, treat as "too small"
        return None

    if size_bytes < 70 * 1024:  # 80KB threshold in bytes
        try:
            shutil.rmtree(output_dir)
            delete_empty_dirs(os.path.dirname(output_dir), polish_dir)
            return None
        except Exception as e:
            print(f"Error during cleanup: {e}")
            return None
    else:
        if not generate_image_prompt(fname_image, output_dir):
            try:
                shutil.rmtree(output_dir)
                delete_empty_dirs(os.path.dirname(output_dir), polish_dir)
                return None
            except Exception as e:
                print(f"Error during cleanup: {e}")
                return None

    return output_dir

