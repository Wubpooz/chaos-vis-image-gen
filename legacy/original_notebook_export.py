def process_single_attractor(password, output_dir, max_iter=10**5):
    """
    Attempts to generate a random attractor. If it meets certain conditions,
    saves and returns the directory. Otherwise returns None.
    """

    # Generate random attractor parameters
    np.random.seed(int(random.random() * 1e6))
    ax = 4 * (np.random.rand(6) - 0.5)
    ay = 4 * (np.random.rand(6) - 0.5)
    xmin, xmax = 1e32, -1e32
    ymin, ymax = 1e32, -1e32
    x0, y0 = np.random.rand() - 0.5, np.random.rand() - 0.5

    lyapunov = 0

    # Initialize trajectory
    x_vals = np.zeros(max_iter)
    y_vals = np.zeros(max_iter)
    x_vals[0], y_vals[0] = x0, y0

    # For lyapunov
    while True:
        xe = x_vals[0] + (np.random.rand() - 0.5) / 1000.0
        ye = y_vals[0] + (np.random.rand() - 0.5) / 1000.0
        dx, dy = x_vals[0] - xe, y_vals[0] - ye
        d0 = np.sqrt(dx**2 + dy**2)
        if d0 > 0:
            break

    drawit = True

    for i in range(1, max_iter):
        # Update primary system
        x_vals[i] = (ax[0] + ax[1]*x_vals[i-1] + ax[2]*x_vals[i-1]**2 +
                     ax[3]*x_vals[i-1]*y_vals[i-1] + ax[4]*y_vals[i-1] +
                     ax[5]*y_vals[i-1]**2)
        y_vals[i] = (ay[0] + ay[1]*x_vals[i-1] + ay[2]*x_vals[i-1]**2 +
                     ay[3]*x_vals[i-1]*y_vals[i-1] + ay[4]*y_vals[i-1] +
                     ay[5]*y_vals[i-1]**2)

        # For lyapunov
        xenew = (ax[0] + ax[1]*xe + ax[2]*xe**2 + ax[3]*xe*ye + ax[4]*ye +
                 ax[5]*ye**2)
        yenew = (ay[0] + ay[1]*xe + ay[2]*xe**2 + ay[3]*xe*ye + ay[4]*ye +
                 ay[5]*ye**2)

        # Track min/max
        xmin = min(xmin, x_vals[i])
        ymin = min(ymin, y_vals[i])
        xmax = max(xmax, x_vals[i])
        ymax = max(ymax, y_vals[i])

        # If unbounded, discard
        if xmin < -1e10 or ymin < -1e10 or xmax > 1e10 or ymax > 1e10:
            drawit = False
            break

        dx, dy = x_vals[i] - x_vals[i-1], y_vals[i] - y_vals[i-1]
        if abs(dx) < 1e-10 and abs(dy) < 1e-10:
            drawit = False
            break

        # Start computing lyapunov after 2000 steps
        if i > 2000:
            dx, dy = x_vals[i] - xenew, y_vals[i] - yenew
            dd = np.sqrt(dx**2 + dy**2)
            if dd > 0:
                lyapunov += np.log(abs(dd / d0))
            xe, ye = x_vals[i] + d0 * dx / (dd if dd != 0 else 1e-15), y_vals[i] + d0 * dy / (dd if dd != 0 else 1e-15)

    # Some conditions for "interesting" attractors
    if drawit:
        # If lyapunov is near zero or negative, skip
        if abs(lyapunov) < 10:
            drawit = False
        elif lyapunov < 0:
            drawit = False

    if drawit:
        return save_attractor(password, ax, ay, xmin, xmax, ymin, ymax, x_vals, y_vals, output_dir)

    return None

