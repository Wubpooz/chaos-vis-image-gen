@njit
def set_pixel_and_neighbors(img, px, py, val_center=1.0, val_neighbor=0.6):
    if 0 <= px < img.shape[1] and 0 <= py < img.shape[0]:
        img[py, px] = val_center
        for dx in (-1, 0, 1):
            for dy in (-1, 0, 1):
                nx = px + dx
                ny = py + dy
                if 0 <= nx < img.shape[1] and 0 <= ny < img.shape[0]:
                    if not (nx == px and ny == py):
                        if img[ny, nx] < val_neighbor:
                            img[ny, nx] = val_neighbor

@njit
def draw_line(img, px0, py0, px1, py1):
    dx = px1 - px0
    dy = py1 - py0
    steps_line = max(abs(dx), abs(dy)) + 1
    xs = np.round(np.linspace(px0, px1, steps_line)).astype(np.int32)
    ys = np.round(np.linspace(py0, py1, steps_line)).astype(np.int32)
    for x_lin, y_lin in zip(xs, ys):
        set_pixel_and_neighbors(img, x_lin, y_lin)

